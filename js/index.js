function index_select(){
    let i = 0;
    var cookies = document.cookie.split(";");
    for(; i < cookies.length; ++i){
        var dc = cookies[i].split("=");
        if(dc[0]=="select"){
            break;
        }
    }
    if(i == cookies.length){
        document.cookie = "select=1";
    }
    let obj = document.getElementsByClassName("elems");
    for(let j = 0 ; j < obj.length; ++j){
        var dc = localStorage.getItem("post"+(j+1));
        if(dc != null){
            dc = JSON.parse(dc);
            var e_n = obj[j].getElementsByClassName('e_n');
            var e_cn = obj[j].getElementsByClassName('e_cn');
            var e_w = obj[j].getElementsByClassName('e_w');
            e_n[0].innerHTML = dc.info_0;
            e_cn[0].innerHTML = dc.info_1;
            e_w[0].innerHTML = dc.info_2;
            if(dc.info_5 == '碱金属' || dc.info_5 == '碱土金属'|| dc.info_5 == '过渡金属'|| dc.info_5 == '镧系元素'|| dc.info_5 == '锕系元素'|| dc.info_5 == '主族金属'){
                obj[j].style.backgroundColor = "rgb(39, 248, 39)";
            }else if(dc.info_5 == '半金属'){
                obj[j].style.backgroundColor = "rgb(23, 190, 23)";
            }else if(dc.info_5 == '非金属' || dc.info_5 == '卤素' || dc.info_5 == '稀有气体'){
                obj[j].style.backgroundColor = "rgb(5, 148, 5)";
            }else{
                obj[j].style.backgroundColor = "rgb(207, 194, 134)";
            }
            if(dc.info_3 == '' && dc.info_4 == ''){
                e_n[0].style.color = "grey";
                e_cn[0].style.color = "grey";
                e_w[0].style.color = "grey";
            }else if(parseInt(dc.info_4) <= 25){
                e_n[0].style.color = "deepskyblue";
                e_cn[0].style.color = "deepskyblue";
                e_w[0].style.color = "deepskyblue";
            }else if(parseInt(dc.info_3) <= 25){
                e_n[0].style.color = "blue";
                e_cn[0].style.color = "blue";
                e_w[0].style.color = "blue";
            }else{
                e_n[0].style.color = "black";
                e_cn[0].style.color = "black";
                e_w[0].style.color = "black";
            }
            if(dc.rainfo == '0'){
                obj[j].style.borderColor = "black";
            }else{
                obj[j].style.borderColor = "red";
            }
            obj[j].onclick = () =>{
                let m = "select=" + (j+1);
                document.cookie = m;
                console.log(m);
                window.location.replace('detail.html');
            }
        }
    }
}

